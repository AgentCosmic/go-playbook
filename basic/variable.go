package main

import "fmt"

func main() {
	var a int      // declaring a variable, it will be set to the data type zero value
	var b int = 10 // decalring and initialising the variable
	c := 20        //  shorthand operator

	fmt.Println(a)
	fmt.Println(b)
	fmt.Println(c)

	var m int64 = 2223232
	var n float64 = 22.20
	var str string = "Hello World"

	fmt.Println(m)
	fmt.Println(n)
	fmt.Println(str)
}
